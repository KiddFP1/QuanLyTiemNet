-- Hiển thị danh sách các cột của bảng ServiceOrder
SELECT COLUMN_NAME, DATA_TYPE, CHARACTER_MAXIMUM_LENGTH, IS_NULLABLE
FROM INFORMATION_SCHEMA.COLUMNS
WHERE TABLE_NAME = 'ServiceOrder'
ORDER BY ORDINAL_POSITION;
GO

